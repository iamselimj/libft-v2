#include "libft.h"

t_int64	ft_putnbr_uns(t_uint64 num)
{
	t_int64	len;

	len = 0;
	if (num >= 10)
		len += ft_putnbr_uns(num / 10);
	len += ft_putchar((num % 10) + '0');
	return (len);
}
