#include "libft.h"

t_uint64	ft_numlen(long num)
{
	t_uint64	len;

	len = 0;
	if (num <= 0)
	{
		num *= -1;
		len++;
	}
	while (num > 0)
	{
		num /= 10;
		len++;
	}
	return (len);
}
